package modelo;

import java.io.Serializable;

public class ServidorCaido implements Serializable {
	private String nombre;
	private String nombrePropio;
	
	public ServidorCaido() {
		super();
	}

	public String getNombre() {
		return nombre;
	}

	public String getNombrePropio() {
		return nombrePropio;
	}
	
	
	

}
